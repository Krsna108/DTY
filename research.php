          <h1 left-align>Research and Publications</h1>      
                      
          <p>Diaspora studies is an academic field established in the late 20th century to study dispersed ethnic populations, which are often termed diaspora peoples. The usage of the term diaspora carries the connotation of forced resettlement, due to expulsion, coercion, slavery, racism, or war, especially nationalist conflicts.</p>

